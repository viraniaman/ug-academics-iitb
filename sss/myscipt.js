$(document).ready(function(){
  $(".rr1").mouseover(function(){
  
   
       $("#apDiv10").fadeIn(10);
    $("#apDiv10").animate({
      left:'15%',
      opacity:'0.8',
      height:'150px',
      width:'150px'
    });
     });
$(".rr1").mouseleave(function(){
    
     $("#apDiv10").fadeOut(10); 
$("#apDiv10").animate({
  left:'12%',
     opacity:'1',
     height: '100px',
     width:'100px'
    });
   $("#apDiv10").fadeOut(100);
   
  });
 
});

 
