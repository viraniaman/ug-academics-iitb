FAQs<br>
Q. Is it compulsory to upload a resume?<br>
A. No, it is not compulsory to upload a resume, but we strongly 
recommend it because it help your 
application to the Professor by giving a holistic picture of your prior
experience and capabilities.
<br><br>
Q. What is the duration of ISPA?<br>
A. The duration of ISPA is a mutual decision between the Professor and 
you. You can put forth your convenient dates during the interview and 
the Professor can comment on his/her availability. Ideally it is one and 
a half to two months. It can also be exceeded into the semester in case 
the student is enthusiastic enough and wants to work for a URA award.
<br><br>
Q. What should I write in a SOP?<br>
A. Statement of Purpose should reflect the motivations with which you 
are filling the application for the project you want to do. It is used 
by the Professor to judge if you are enthusiastic enough to learn and 
work for the entire duration of the project.
<br><br>
Q. Can PGs also participate in ISPA?<br>
A. No, ISPA is only intended for UG students.
